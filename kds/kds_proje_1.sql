    DECLARE tahmini_kar DECIMAL(15,2);
    DECLARE ortalama_not INT;

    -- 1. KAMPÜS SENARYOSU: Üniversiteye yakın şubelerin ortalamasını baz al
    IF p_senaryo = 'kampus' THEN
        SELECT AVG(spo.toplam_ciro), AVG(spo.net_kar)
        INTO tahmini_ciro, tahmini_kar
        FROM sube_performans_ozeti spo
        JOIN subeler s ON s.sube_id = spo.sube_id
        WHERE s.yakin_uni_yurt_var_mi = 1
        AND spo.donem_ay_yil LIKE '2025%'; -- Sadece güncel yıl verileri

    -- 2. AVM SENARYOSU: AVM'ye yakın şubelerin ortalamasını baz al
    ELSEIF p_senaryo = 'avm' THEN
        SELECT AVG(spo.toplam_ciro), AVG(spo.net_kar)
        INTO tahmini_ciro, tahmini_kar
        FROM sube_performans_ozeti spo
        JOIN subeler s ON s.sube_id = spo.sube_id
        WHERE s.yakin_avm_is_merkezi_var_mi = 1
        AND spo.donem_ay_yil LIKE '2025%';

    -- 3. CADDE SENARYOSU: Diğer şubelerin ortalaması
    ELSE
        SELECT AVG(spo.toplam_ciro), AVG(spo.net_kar)
        INTO tahmini_ciro, tahmini_kar
        FROM sube_performans_ozeti spo
        JOIN subeler s ON s.sube_id = spo.sube_id
        WHERE s.yakin_avm_is_merkezi_var_mi = 0 AND s.yakin_uni_yurt_var_mi = 0
        AND spo.donem_ay_yil LIKE '2025%';
    END IF;

    -- Eğer hiç veri yoksa (NULL dönerse) varsayılan değer ata
    IF tahmini_ciro IS NULL THEN SET tahmini_ciro = 50000; END IF;
    IF tahmini_kar IS NULL THEN SET tahmini_kar = 10000; END IF;

    -- Sonucu Döndür
    SELECT tahmini_ciro as ciro, tahmini_kar as kar;

END$$

